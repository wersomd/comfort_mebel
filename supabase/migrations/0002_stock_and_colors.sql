-- ── Stock ───────────────────────────────────────────────────────────────────
-- Общий остаток товара (если нет цветных вариантов)
-- NULL = "наличие неизвестно/не учитываем"; число >= 0 = реальный остаток
alter table public.products
  add column if not exists stock int;

-- ── Цветные варианты ────────────────────────────────────────────────────────
-- jsonb-массив объектов вида:
-- [{ "id": "c1", "name": "Серый", "hex": "#888888",
--    "images": ["url1", "url2"], "stock": 5 }, ...]
-- Если массив пуст — используется обычный товар (без вариантов).
alter table public.products
  add column if not exists colors jsonb not null default '[]'::jsonb;
