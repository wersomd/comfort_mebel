-- ── Размерные варианты ───────────────────────────────────────────────────────
-- jsonb-массив объектов вида:
-- [{ "id": "s1", "label": "Угловой", "dimensions": "300x200x85 см",
--    "price": 690000, "oldPrice": null, "images": ["url1"], "stock": 3 }, ...]
-- Если массив пуст — у товара один размер (используются общие цена/габариты).
-- Цвет и размер выбираются независимо: фото берётся от размера → иначе от цвета
-- → иначе общие фото; цена — от размера, иначе базовая product.price.
alter table public.products
  add column if not exists sizes jsonb not null default '[]'::jsonb;
